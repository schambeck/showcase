package net.unibave.showcase.base.enumeration;

public class EnumChaveValor {
    
    private String chave;
    
    private String valor;

    public EnumChaveValor(String chave, String valor) {
        this.chave = chave;
        this.valor = valor;
    }

    public String getChave() {
        return chave;
    }

    public String getValor() {
        return valor;
    }
    
}
